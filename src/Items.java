public class Items {
        String name;
        int price;
        int number;

        public Items(String name, int price, int number){
                this.name = name;
                this.price = price;
                this.number = number;
        }

        public String getName() {
                return name;
        }

        public int getPrice() {
                return price;
        }

        public int getNumber() {
                return number;
        }


}
